(ns mire.util) ; Объявление пространства имен "mire.util"

(defn pick-rand ; Определение функции pick-rand
      "Return a random element of vect." ; Строка документации - функция возвращает случайный элемент вектора
      [vect] ; Параметр vect - вектор, из которого выбирается случайный элемент
      (vect (rand-int (count vect)))) ; Получение случайного элемента по индексу:
; (count vect) - получает количество элементов в векторе
; (rand-int n) - генерирует случайное целое число от 0 до n-1
; (vect index) - получает элемент вектора по сгенерированному индексу